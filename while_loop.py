#This program calculate the sum of 0 through 9 (including 9).

#Initialize sum.
sum = 0

#Initialize i.
i = 0

while i < 10: #Using while loop and writing the condition of when the program stops.
    sum = sum + i #Writing the formula.
    print(sum) #print the updated sum.
    i = i + 1 #Indicating how the value of i will be increasing.

